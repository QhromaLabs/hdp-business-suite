export function useRestockRawMaterial() {
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: async ({ materialId, quantity, unit_cost }: { materialId: string; quantity: number; unit_cost?: number }) => {
      const { data: existing, error: fetchError } = await supabase
        .from('raw_materials')
        .select('quantity_in_stock, unit_cost')
        .eq('id', materialId)
        .maybeSingle();

      if (fetchError) throw fetchError;

      const currentQty = Number(existing?.quantity_in_stock || 0);
      const newQty = currentQty + quantity;

      const { error } = await supabase
        .from('raw_materials')
        .update({
          quantity_in_stock: newQty,
          unit_cost: typeof unit_cost === 'number' ? unit_cost : existing?.unit_cost || null,
          updated_at: new Date().toISOString(),
        })
        .eq('id', materialId);

      if (error) throw error;
      return { materialId, newQty };
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['raw_materials'] });
      toast.success('Stock updated');
    },
    onError: (error) => {
      toast.error('Failed to restock: ' + error.message);
    },
  });
}

