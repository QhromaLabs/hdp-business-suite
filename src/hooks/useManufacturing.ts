export function useCreateProductionRun() {
  const queryClient = useQueryClient();
  
  return useMutation({
    mutationFn: async (run: { 
      product_id: string; 
      variant_id?: string; 
      machine_id?: string; 
      planned_quantity: number;
      start_date?: string;
      notes?: string;
    }) => {
      const { data, error } = await supabase
        .from('production_runs')
        .insert({
          ...run,
          batch_number: '', // Will be auto-generated by trigger
          status: 'in_progress',
          actual_quantity: 0,
          wastage_quantity: 0,
          start_date: run.start_date || new Date().toISOString(),
        })
        .select()
        .single();
      
      if (error) throw error;
      return data;
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['production_runs'] });
      toast.success('Production run created successfully');
    },
    onError: (error) => {
      toast.error('Failed to create production run: ' + error.message);
    },
  });
}

export function useUpdateProductionRun() {
  const queryClient = useQueryClient();

  return useMutation({
    mutationFn: async ({ 
      id, 
      status, 
      actual_quantity, 
      wastage_quantity 
    }: { 
      id: string; 
      status?: ProductionStatus | null; 
      actual_quantity?: number; 
      wastage_quantity?: number; 
    }) => {
      const updates: Record<string, any> = {
        updated_at: new Date().toISOString(),
      };

      if (typeof status !== 'undefined') {
        updates.status = status;
        updates.end_date = status === 'completed' ? new Date().toISOString() : null;
      }
      if (typeof actual_quantity !== 'undefined') updates.actual_quantity = actual_quantity;
      if (typeof wastage_quantity !== 'undefined') updates.wastage_quantity = wastage_quantity;

      const { error } = await supabase
        .from('production_runs')
        .update(updates)
        .eq('id', id);

      if (error) throw error;
      return { id, ...updates };
    },
    onSuccess: () => {
      queryClient.invalidateQueries({ queryKey: ['production_runs'] });
      toast.success('Production run updated');
    },
    onError: (error) => {
      toast.error('Failed to update production run: ' + error.message);
    },
  });
}

